CREATE TABLE addressbook
(
  `ID` int(11) NOT NULL AUTO_INCREMENT PRIMARY KEY AUTO_INCREMENT,

  `First Name` varchar(128) NOT NULL,

  `Last Name` varchar(128) NOT NULL,

  `Address Line 1` varchar(128) NOT NULL,

  `Address Line` 2 varchar(128) DEFAULT NULL,

  `City` varchar(128) NOT NULL,

  `State` varchar(128) NOT NULL,

  `Zip Code` varchar(128) NOT NULL
)
